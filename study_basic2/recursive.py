#再帰プログラミング

"""
階乗のの例
n! = n*(n-1)! # ok

2! = 2*1 = 2
3! = 3*2*1 = 6
41 = 4*3*2*1 = 24
5! = 5*4*3*2*1 = 120
6! = 6*5*4*3*2*1 = 720
"""

def fact(n):
    print("今のnの値は{0}".format(n))
    #再帰呼び出しの停止条件
    # 0! = 1
    if n == 0 : return 1
    #再帰呼び出す
    print(n)
    return n*fact(n-1)
print(fact(1))
